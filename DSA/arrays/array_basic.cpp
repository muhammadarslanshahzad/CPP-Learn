#include <bits/stdc++.h>
using namespace std;

void arrTraverse(int arr[], int size)
{
    for(int i=0; i<size; i++){
        cout<<arr[i]<<" ";
    }
    cout<<endl;
}

void deleteArr(int arr[])
{
    delete [] arr;
}
int main(void){
    int n=10;
    //dynamic arrays
    int *arr = new int[n];

    for(int i=0; i<n; i++){
        cin>>arr[i];
    }
    
    arrTraverse(arr, n);
    deleteArr(arr);

    return 0;
}

